# ======================================================
# SELECTIVE TEMPORAL ATTENTION
# ======================================================

class SelectiveTemporalAttention(nn.Module):

    def __init__(self, feature_dim):

        super().__init__()

        self.attention = nn.Sequential(

            nn.Linear(feature_dim, 256),

            nn.ReLU(),

            nn.Linear(256, 1)
        )

    def forward(self, temporal_features):

        # ==========================================
        # temporal_features
        # [B, T, F]
        # ==========================================

        attention_scores = self.attention(
            temporal_features
        )

        # ==========================================
        # NORMALIZE SCORES
        # ==========================================

        attention_weights = torch.softmax(
            attention_scores,
            dim=1
        )

        # ==========================================
        # SELECTIVE TEMPORAL FUSION
        # ==========================================

        weighted_features = (
            temporal_features
            *
            attention_weights
        )

        fused_features = weighted_features.sum(
            dim=1
        )

        return fused_features

