import cv2
import numpy as np
import torch


def generate_boundary_mask(mask):

    mask = mask.squeeze().cpu().numpy().astype(np.uint8)

    edges = cv2.Canny(mask * 255, 100, 200)

    edges = edges / 255.0

    return torch.tensor(edges, dtype=torch.float32)

# =========================================================
# BOUNDARY-AWARE HYBRID LOSS
# =========================================================

import torch.nn as nn
import torch.nn.functional as F


class BoundaryHybridLoss(nn.Module):

    def __init__(self):
        super().__init__()

        self.bce = nn.BCEWithLogitsLoss()

    def dice_loss(self, preds, targets, smooth=1e-6):

        preds = torch.sigmoid(preds)

        preds = preds.view(-1)
        targets = targets.view(-1)

        intersection = (preds * targets).sum()

        dice = (
            (2. * intersection + smooth)
            /
            (preds.sum() + targets.sum() + smooth)
        )

        return 1 - dice

    def boundary_loss(self, preds, targets):

        preds = torch.sigmoid(preds)

        boundary_targets = []

        for i in range(targets.shape[0]):

            edge = generate_boundary_mask(targets[i])

            boundary_targets.append(edge)

        boundary_targets = torch.stack(boundary_targets).unsqueeze(1)

        boundary_targets = boundary_targets.to(preds.device)

        boundary_bce = F.binary_cross_entropy(
            preds,
            boundary_targets
        )

        return boundary_bce

    def forward(self, preds, targets):

        bce_loss = self.bce(preds, targets)

        dice = self.dice_loss(preds, targets)

        boundary = self.boundary_loss(preds, targets)

        total_loss = (
            0.4 * bce_loss
            +
            0.4 * dice
            +
            0.2 * boundary
        )

        return total_loss

