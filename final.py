class WaterDataset(Dataset):

    def __init__(self, files):

        self.files = files

    def __len__(self):

        return len(self.files)

    def __getitem__(self, idx):

        path = self.files[idx]

        with rasterio.open(path) as src:

            image = src.read()

        # INPUT
        x = image[:5]

        # MASK
        y = image[5]

        # HANDLE NaNs
        x = np.nan_to_num(x)

        # NORMALIZATION
        for i in range(x.shape[0]):

            band = x[i]

            band_min = band.min()

            band_max = band.max()

            x[i] = (
                (band - band_min)
                /
                (band_max - band_min + 1e-8)
            )

        # TO TENSOR
        x = torch.tensor(
            x,
            dtype=torch.float32
        )

        y = torch.tensor(
            y,
            dtype=torch.float32
        ).unsqueeze(0)

        # RESIZE INPUT
        x = nn.functional.interpolate(
            x.unsqueeze(0),
            size=(IMAGE_SIZE, IMAGE_SIZE),
            mode='bilinear',
            align_corners=False
        ).squeeze(0)

        # RESIZE MASK
        y = nn.functional.interpolate(
            y.unsqueeze(0),
            size=(IMAGE_SIZE, IMAGE_SIZE),
            mode='nearest'
        ).squeeze(0)

        return x, y

