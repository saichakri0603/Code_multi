# ======================================================

class TemporalGRUNet(nn.Module):

    def __init__(self):

        super().__init__()

        # ==========================================
        # SPATIAL ENCODER
        # ==========================================

        self.encoder = CNNEncoder()

        # ==========================================
        # FEATURE SIZE
        # ==========================================

        self.feature_size = 128 * 16 * 16

        # ==========================================
        # GRU TEMPORAL MEMORY
        # ==========================================

        self.gru = nn.GRU(

            input_size=self.feature_size,

            hidden_size=512,

            batch_first=True
        )

        # ==========================================
        # DECODER
        # ==========================================

        self.decoder = nn.Sequential(

            nn.Linear(512, 128 * 16 * 16),

            nn.ReLU()
        )

        # ==========================================
        # SEGMENTATION HEAD
        # ==========================================

        self.seg_head = nn.Sequential(

            nn.ConvTranspose2d(
                128,
                64,
                kernel_size=2,
                stride=2
            ),

            nn.ReLU(),

            nn.ConvTranspose2d(
                64,
                32,
                kernel_size=2,
                stride=2
            ),

            nn.ReLU(),

            nn.ConvTranspose2d(
                32,
                16,
                kernel_size=2,
                stride=2
            ),

            nn.ReLU(),

            nn.Conv2d(
                16,
                1,
                kernel_size=1
            )
        )

    def forward(self, x):

        # ==========================================
        # INPUT SHAPE
        # [B, T, C, H, W]
        # ==========================================

        B, T, C, H, W = x.shape

        encoded_frames = []

        # ==========================================
        # ENCODE EACH FRAME
        # ==========================================

        for t in range(T):

            frame = x[:, t]

            features = self.encoder(frame)

            features = features.view(B, -1)

            encoded_frames.append(features)

        # ==========================================
        # STACK TEMPORAL FEATURES
        # ==========================================

        temporal_features = torch.stack(
            encoded_frames,
            dim=1
        )

        # ==========================================
        # GRU
        # ==========================================

        gru_out, hidden = self.gru(
            temporal_features
        )

        # ==========================================
        # LAST TEMPORAL STATE
        # ==========================================

        final_state = hidden[-1]

        # ==========================================
        # DECODER
        # ==========================================

        decoded = self.decoder(final_state)

        decoded = decoded.view(
            B,
            128,
            16,
            16
        )

        # ==========================================
        # SEGMENTATION
        # ==========================================

        output = self.seg_head(decoded)

        return output

