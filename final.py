class TemporalConvLSTMUNet(nn.Module):

    def __init__(self):

        super().__init__()

        # ==========================================
        # ENCODER
        # ==========================================

        self.encoder = SpatialEncoder()

        # ==========================================
        # CONVLSTM
        # ==========================================

        self.convlstm = ConvLSTMCell(

            input_dim=128,

            hidden_dim=128
        )

        # ==========================================
        # DECODER
        # ==========================================

        self.decoder = nn.Sequential(

            nn.ConvTranspose2d(
                128,
                64,
                kernel_size=2,
                stride=2
            ),

            nn.ReLU(),

            nn.ConvTranspose2d(
                64,
                32,
                kernel_size=2,
                stride=2
            ),

            nn.ReLU(),

            nn.Conv2d(
                32,
                1,
                kernel_size=1
            )
        )

    def forward(self, x):

        # ==========================================
        # INPUT
        # [B, T, C, H, W]
        # ==========================================

        B, T, C, H, W = x.shape

        h = torch.zeros(

            B,
            128,
            H // 4,
            W // 4

        ).to(x.device)

        c = torch.zeros_like(h)

        temporal_outputs = []

        # ==========================================
        # PROCESS TEMPORAL FRAMES
        # ==========================================

        for t in range(T):

            frame = x[:, t]

            features = self.encoder(frame)

            h, c = self.convlstm(
                features,
                h,
                c
            )

            pred = self.decoder(h)

            temporal_outputs.append(pred)

        # ==========================================
        # STACK TEMPORAL OUTPUTS
        # [B, T, 1, H, W]
        # ==========================================

        temporal_outputs = torch.stack(
            temporal_outputs,
            dim=1
        )

        return temporal_outputs

