def temporal_consistency_loss(predictions):

    """
    predictions:
    [B, T, 1, H, W]
    """

    consistency = 0

    T = predictions.shape[1]

    for t in range(T - 1):

        current_pred = predictions[:, t]

        next_pred = predictions[:, t + 1]

        consistency += torch.mean(
            torch.abs(
                current_pred - next_pred
            )
        )

    consistency /= (T - 1)

    return consistency

