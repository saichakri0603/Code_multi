class ConvLSTMCell(nn.Module):

    def __init__(

        self,

        input_dim,

        hidden_dim,

        kernel_size=3
    ):

        super().__init__()

        padding = kernel_size // 2

        self.hidden_dim = hidden_dim

        self.conv = nn.Conv2d(

            input_dim + hidden_dim,

            4 * hidden_dim,

            kernel_size=kernel_size,

            padding=padding
        )

    def forward(

        self,

        x,

        h_prev,

        c_prev
    ):

        combined = torch.cat(
            [x, h_prev],
            dim=1
        )

        gates = self.conv(combined)

        i, f, o, g = torch.chunk(
            gates,
            4,
            dim=1
        )

        i = torch.sigmoid(i)

        f = torch.sigmoid(f)

        o = torch.sigmoid(o)

        g = torch.tanh(g)

        c = f * c_prev + i * g

        h = o * torch.tanh(c)

        return h, c

