class ConvLSTMUNet(nn.Module):

    def __init__(self):

        super().__init__()

        # ==========================================
        # SPATIAL ENCODER
        # ==========================================

        self.encoder = SpatialEncoder()

        # ==========================================
        # CONVLSTM
        # ==========================================

        self.convlstm = ConvLSTMCell(

            input_dim=128,

            hidden_dim=128
        )

        # ==========================================
        # DECODER
        # ==========================================

        self.decoder = nn.Sequential(

            nn.ConvTranspose2d(
                128,
                64,
                kernel_size=2,
                stride=2
            ),

            nn.ReLU(),

            nn.ConvTranspose2d(
                64,
                32,
                kernel_size=2,
                stride=2
            ),

            nn.ReLU(),

            nn.Conv2d(
                32,
                1,
                kernel_size=1
            )
        )

    def forward(self, x):

        # ==========================================
        # INPUT
        # [B, T, C, H, W]
        # ==========================================

        B, T, C, H, W = x.shape

        # ==========================================
        # INITIAL STATES
        # ==========================================

        h = torch.zeros(

            B,
            128,
            H // 4,
            W // 4

        ).to(x.device)

        c = torch.zeros_like(h)

        # ==========================================
        # TEMPORAL PROCESSING
        # ==========================================

        for t in range(T):

            frame = x[:, t]

            features = self.encoder(frame)

            h, c = self.convlstm(
                features,
                h,
                c
            )

        # ==========================================
        # DECODER
        # ==========================================

        output = self.decoder(h)

        return output

