SMOOTH = 1e-6

def calculate_metrics(preds, targets):

    preds = (preds > 0.5).astype(np.uint8)

    targets = targets.astype(np.uint8)

    preds_flat = preds.flatten()

    targets_flat = targets.flatten()

    intersection = (
        preds_flat * targets_flat
    ).sum()

    dice = (
        (2. * intersection + SMOOTH)
        /
        (
            preds_flat.sum()
            +
            targets_flat.sum()
            +
            SMOOTH
        )
    )

    iou = (
        (intersection + SMOOTH)
        /
        (
            preds_flat.sum()
            +
            targets_flat.sum()
            -
            intersection
            +
            SMOOTH
        )
    )

    acc = accuracy_score(
        targets_flat,
        preds_flat
    )

    precision = precision_score(
        targets_flat,
        preds_flat,
        zero_division=0
    )

    recall = recall_score(
        targets_flat,
        preds_flat,
        zero_division=0
    )

    f1 = f1_score(
        targets_flat,
        preds_flat,
        zero_division=0
    )

    return (
        dice,
        iou,
        acc,
        precision,
        recall,
        f1
    )

